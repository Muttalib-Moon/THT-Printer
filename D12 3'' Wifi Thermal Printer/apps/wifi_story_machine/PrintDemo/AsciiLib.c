void SelfPrintWriteLines(unsigned char *BufferStr)
{
	unsigned char DotsASCII[48];
	unsigned int Read=0,Write4Dots=0;
	unsigned int GapDots=4;			//line gap dots
	unsigned char *MatricDot;		//line Printing buffer
	unsigned int x,y;
	unsigned int LineBytes=72;
	MatricDot=(unsigned char*)malloc(sizeof(unsigned char)*(24+GapDots)*LineBytes); //total buffer for one line characters
	memset(MatricDot,0,sizeof(unsigned char)*(24+GapDots)*LineBytes);//clear the matric dots buffer
	while(BufferStr[Read]!='\0'&&Write4Dots<LineBytes*2)//loop condition
	{
		GetASCIICode(DotsASCII,BufferStr[Read++]);//fetch dot matrics by ASCII character
		for(x=0;x<2;x++)//horizental loop
		{
			for(y=0;y<24;y++)//vertical loop
			{
				if(Write4Dots%2==0) //
				{
					if(x==0)
					{
						MatricDot[LineBytes*y+Write4Dots/2]|=(DotsASCII[y*2+x]&0xf0);
						MatricDot[LineBytes*y+(Write4Dots+1)/2]|=(DotsASCII[y*2+x]&0x0f);
					}
					else
					{
						MatricDot[LineBytes*y+(Write4Dots+2)/2]|=((DotsASCII[y*2+x]&0xf0));
					}
				}
				else
				{
					if(x==0)
					{
						MatricDot[LineBytes*y+Write4Dots/2]|=((DotsASCII[y*2+x]&0xf0)>>4);
						MatricDot[LineBytes*y+(Write4Dots+1)/2]|=((DotsASCII[y*2+x]&0x0f)<<4);
					}
					else
					{
						MatricDot[LineBytes*y+(Write4Dots+2)/2]|=((DotsASCII[y*2+x]&0xf0)>>4);
					}
				}
			}
		}
		Write4Dots+=3;
		if(Write4Dots>=LineBytes*2)
		{
			printf("Write4Dots:%d-Read:%d>>",Write4Dots,Read);
			break;
		}
	}
	printf("Write4Dots:%d-Read:%d>>",Write4Dots,Read);
	for(y=0;y<24+GapDots;y++)
	{
		WritePrintLinkedListB(MatricDot+y*LineBytes,LineBytes);
	}
	free(MatricDot);
}
